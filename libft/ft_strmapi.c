#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*result;
	int		i;
	int		leng;

	i = 0;
	if (!s || !f)
		return (NULL);
	leng = ft_strlen(s);
	result = malloc(leng + 1 * sizeof (char));
	if (!result)
		return (NULL);
	while (s[i] != '\0')
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
